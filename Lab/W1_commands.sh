This document covers some essential Linux commands and their usage inside the Kali Linux terminal.

## Commands and Usage

### `ip a`

Displays the IP addresses assigned to your system.

```bash
ip a
```

### `cd`

Changes the current directory.

```bash
cd Downloads
```

Moves you into the **Downloads** folder.

### `touch`

Creates a new empty file.

```bash
touch sak
```

### `echo`

Adds contents to a file.

```bash
echo "hi" > sak
```

### `cat`

Prints the contents of a file.

```bash
cat sak
```

### `whoami`

Shows the current logged-in username.

```bash
whoami
```

### `ls -la`

Lists all files in the current directory, including hidden files (`.` dotfiles), with details like permissions, owner, size, and modified date.

```bash
ls -la
```

### `pwd`

Prints the current working directory (the folder you are in).

```bash
pwd
```

---

## Notes

* These commands are fundamental for navigating and managing files in Kali Linux.
* Combining commands with **redirection (`>`, `>>`)** and **pipes (`|`)** will allow more advanced workflows (to be covered later).
* Always double-check file paths and permissions when using commands that create or modify files.

